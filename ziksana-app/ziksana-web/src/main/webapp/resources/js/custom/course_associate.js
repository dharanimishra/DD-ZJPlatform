function playVideo(path) {

	$.fancybox({

		'width' : 660,

		'height' : 500,

		'autoDimensions' : true,

		'transitionIn' : 'fade',

		'transitionOut' : 'fade',

		'type' : 'iframe',

		'href' : path

	});
}

