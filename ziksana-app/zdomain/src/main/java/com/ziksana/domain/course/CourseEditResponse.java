package com.ziksana.domain.course;

public class CourseEditResponse {

	private String id;
	private String coursename;
	private String coursedesc;
	private String subjectarea;
	private String subject;
	private String topic;
	private String tagfield;
	private String credits;
	private String extracredits;
	private String duration;
	private String durationtype;
	private String imageupload;
	private String response;
	private String message;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCoursename() {
		return coursename;
	}

	public void setCoursename(String coursename) {
		this.coursename = coursename;
	}

	public String getCoursedesc() {
		return coursedesc;
	}

	public void setCoursedesc(String coursedesc) {
		this.coursedesc = coursedesc;
	}

	public String getSubjectarea() {
		return subjectarea;
	}

	public void setSubjectarea(String subjectarea) {
		this.subjectarea = subjectarea;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getTopic() {
		return topic;
	}

	public void setTopic(String topic) {
		this.topic = topic;
	}

	public String getTagfield() {
		return tagfield;
	}

	public void setTagfield(String tagfield) {
		this.tagfield = tagfield;
	}

	public String getCredits() {
		return credits;
	}

	public void setCredits(String credits) {
		this.credits = credits;
	}

	public String getExtracredits() {
		return extracredits;
	}

	public void setExtracredits(String extracredits) {
		this.extracredits = extracredits;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public String getDurationtype() {
		return durationtype;
	}

	public void setDurationtype(String durationtype) {
		this.durationtype = durationtype;
	}

	public String getImageupload() {
		return imageupload;
	}

	public void setImageupload(String imageupload) {
		this.imageupload = imageupload;
	}

	public String getResponse() {
		return response;
	}

	public void setResponse(String response) {
		this.response = response;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
