package com.ziksana.domain.sample;

public class Course {
	
	String courseId;
	String courseName;
	String courseDescription;
	String educatorFullName;
	String courseImageName;
	
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseDescription() {
		return courseDescription;
	}
	public void setCourseDescription(String courseDescription) {
		this.courseDescription = courseDescription;
	}
	public String getEducatorFullName() {
		return educatorFullName;
	}
	public void setEducatorFullName(String educatorFullName) {
		this.educatorFullName = educatorFullName;
	}
	public String getCourseImageName() {
		return courseImageName;
	}
	public void setCourseImageName(String courseImageName) {
		this.courseImageName = courseImageName;
	}
}
