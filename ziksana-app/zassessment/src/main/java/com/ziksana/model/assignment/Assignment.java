package com.ziksana.model.assignment;

public class Assignment {
	
	private Integer id;
	private String name;
	private String content;
	private Integer learningComponentId;
	private Integer creatingMemberRoleId;
	
	
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Integer getLearningComponentId() {
		return learningComponentId;
	}
	public void setLearningComponentId(Integer learningComponentId) {
		this.learningComponentId = learningComponentId;
	}
	public Integer getCreatingMemberRoleId() {
		return creatingMemberRoleId;
	}
	public void setCreatingMemberRoleId(Integer creatingMemberRoleId) {
		this.creatingMemberRoleId = creatingMemberRoleId;
	}
	
	
	
	
	

}
